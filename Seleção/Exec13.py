diaSemana = int(input("Entre com o dia da semana. (Ex: 1 - Domingo, 2 - Segunda ... ): "))

if diaSemana == 1:
    print("1 - Domingo!")
elif diaSemana == 2:
    print("2 - Segunda!")
elif diaSemana == 3:
    print("3 - Terça!")
elif diaSemana == 4:
    print("4 - Quarta!")
elif diaSemana == 5:
    print("5 - Quinta!")
elif diaSemana == 6:
    print("6 - Sexta!")
elif diaSemana == 7:
    print("7 - Sábado!")
else:
    print("Dia da semana inválido!")